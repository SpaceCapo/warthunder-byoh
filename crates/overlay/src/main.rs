/// Per-window state kept by the event loop.
#[cfg(all(feature = "render", feature = "windows-glue", not(feature = "gpu")))]
struct WinState {
    window: Arc<Window>,
    /// Index into the shared `Vec<Vec<DisplayRow>>`.
    idx: usize,
    click_through: bool,
}

#[cfg(all(feature = "render", feature = "windows-glue", not(feature = "gpu")))]
struct OverlayApp {
    shared: Shared,
    /// Window configs from indicators.json (used for initial creation).
    window_defs: Vec<WindowDef>,
    /// Live window states, keyed by winit WindowId.
    windows: HashMap<WindowId, WinState>,
    /// Click-through state (applied to all windows together via Insert key).
    click_through: bool,
    last_redraw: Instant,
    /// Whether War Thunder is currently the foreground window.
    wt_foreground: Arc<std::sync::atomic::AtomicBool>,
    /// Whether a mission is actively running (from /mission.json).
    mission_running: Arc<std::sync::atomic::AtomicBool>,
    /// Application config loaded at startup.
    app_config: core_client::AppConfig,
    /// Cached visibility — avoids redundant set_visible calls.
    overlays_visible: bool,
}

#[cfg(all(feature = "render", feature = "windows-glue", not(feature = "gpu")))]
impl OverlayApp {
    fn new(
        shared: Shared,
        window_defs: Vec<WindowDef>,
        wt_foreground: Arc<std::sync::atomic::AtomicBool>,
        mission_running: Arc<std::sync::atomic::AtomicBool>,
        app_config: core_client::AppConfig,
    ) -> Self {
        Self {
            shared,
            window_defs,
            windows: HashMap::new(),
            click_through: false,
            last_redraw: Instant::now(),
            wt_foreground,
            mission_running,
            app_config,
            overlays_visible: true,
        }
    }
}

#[cfg(all(feature = "render", feature = "windows-glue", not(feature = "gpu")))]
impl ApplicationHandler for OverlayApp {
    fn resumed(&mut self, event_loop: &ActiveEventLoop) {
        if !self.windows.is_empty() { return; }

        // Ensure we have at least one window def (fallback if config empty)
        let defs: Vec<_> = if self.window_defs.is_empty() {
            vec![WindowDef {
                id: "overlay".to_string(),
                x: 100, y: 100,
                width: None, height: None,
                indicators: Vec::new(),
                style: None,
            }]
        } else {
            self.window_defs.clone()
        };

        for (idx, wd) in defs.iter().enumerate() {
            let w = wd.computed_width() as f32;
            let h = wd.computed_height() as f32;
            let attrs = WindowAttributes::default()
                .with_title(format!("WT {}", wd.id))
                .with_decorations(false)
                .with_window_level(WindowLevel::AlwaysOnTop)
                .with_inner_size(LogicalSize::new(w, h))
                .with_position(LogicalPosition::new(wd.x, wd.y));

            let window = match event_loop.create_window(attrs) {
                Ok(w) => w,
                Err(e) => { eprintln!("[overlay] create_window '{}': {e}", wd.id); continue; }
            };
            window.set_window_icon(app_icon());

            win32::make_layered(&window);
            let size = window.inner_size();
            if let Some(hwnd) = win32::get_hwnd(&window) {
                paint_layered(hwnd, &[], size.width.max(1), size.height.max(1), None);
            }

            let id = window.id();
            self.windows.insert(id, WinState {
                window: Arc::new(window),
                idx,
                click_through: false,
            });
        }
    }

    fn window_event(
        &mut self,
        event_loop: &ActiveEventLoop,
        id: WindowId,
        event: WindowEvent,
    ) {
        match event {
            WindowEvent::CloseRequested => event_loop.exit(),

            WindowEvent::KeyboardInput { event: ref key_event, .. }
                if key_event.state == ElementState::Pressed
                    && key_event.physical_key == PhysicalKey::Code(KeyCode::Insert) =>
            {
                self.click_through = !self.click_through;
                let ct = self.click_through;
                // Apply to all windows
                for ws in self.windows.values() {
                    win32::set_click_through(&ws.window, ct);
                }
                eprintln!("[overlay] click-through: {ct}");
            }

            WindowEvent::MouseInput {
                state: ElementState::Pressed,
                button: MouseButton::Left, ..
            } => {
                if let Some(ws) = self.windows.get(&id) {
                    if !ws.click_through && !self.click_through {
                        let _ = ws.window.drag_window();
                    }
                }
            }

            WindowEvent::RedrawRequested => {
                if let Some(ws) = self.windows.get(&id) {
                    let guard = self.shared.load();
                    let (rows, win_style): (&[DisplayRow], Option<&core_client::RenderStyle>) =
                        if self.overlays_visible {
                            if let Some(w) = guard.get(ws.idx) {
                                (w.rows.as_slice(), w.style.as_ref())
                            } else {
                                (&[], None)
                            }
                        } else {
                            (&[], None) // empty rows → paint_layered writes fully transparent pixels
                        };
                    let size = ws.window.inner_size();
                    if let Some(hwnd) = win32::get_hwnd(&ws.window) {
                        paint_layered(hwnd, rows, size.width.max(1), size.height.max(1), win_style);
                    }
                }
            }

            _ => {}
        }
    }

    fn about_to_wait(&mut self, _event_loop: &ActiveEventLoop) {
        let wt_fg      = self.wt_foreground.load(std::sync::atomic::Ordering::Relaxed);
        let mission_ok = self.mission_running.load(std::sync::atomic::Ordering::Relaxed);

        let should_show = if self.app_config.always_show {
            true
        } else {
            let show = wt_fg;
            let hide = self.app_config.only_during_mission && !mission_ok;
            show && !hide
        };

        let focus_changed = should_show != self.overlays_visible;
        if focus_changed {
            self.overlays_visible = should_show;
        }

        // Redraw on focus change (clears or restores) or on the 16ms tick while visible.
        let should_redraw = focus_changed
            || (should_show && self.last_redraw.elapsed() >= Duration::from_millis(16));
        if should_redraw {
            self.last_redraw = Instant::now();
            for ws in self.windows.values() {
                ws.window.request_redraw();
            }
        }
    }
}

