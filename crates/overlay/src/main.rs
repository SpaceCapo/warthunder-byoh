/// Build a `winit::window::Icon` from the compile-time RGBA data.
/// Returns `None` on platforms where winit doesn't support icons (graceful).
fn app_icon() -> Option<winit::window::Icon> {
    winit::window::Icon::from_rgba(ICON_RGBA.to_vec(), ICON_WIDTH, ICON_HEIGHT).ok()
}

