//! War Thunder overlay
//!
//! Feature matrix:
//!
//! | Features                        | Backend       | Platforms        |
//! |---------------------------------|---------------|------------------|
//! | `render,gpu`                    | wgpu+glyphon  | Win/Linux/macOS  |
//! | `render,windows-glue` (no gpu)  | GDI layered   | Windows only     |
//! | `render` (no gpu, no win-glue)  | CLI fallback  | Linux/macOS      |
//! | (no render)                     | CLI poller    | all              |
//!
//! ## Data-driven multi-window rendering
//!
//! 1. `indicators.json` is an array of **window** objects, each with an `(x, y)`
//!    position and a list of indicator definitions.
//! 2. One `winit::Window` is created per entry at the configured screen position.
//! 3. A single poller thread calls `client.fetch_display_windows()` every 16 ms
//!    and atomically stores the result as `Arc<Vec<Vec<DisplayRow>>>`.
//! 4. The render loop reads that snapshot and paints each window independently.
//!
//! ## Controls
//! - **Left-click drag** on any window to move it.
//! - **Insert** key toggles click-through mode (all windows simultaneously).

