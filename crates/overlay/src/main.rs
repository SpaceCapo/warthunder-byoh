#[allow(dead_code)]
fn run_cli_mode() {
    let client = Arc::new(Client::new(None).expect("core client"));
    let mut last = Instant::now();
    loop {
        if last.elapsed() > Duration::from_secs(1) {
            last = Instant::now();
            let rows = client.fetch_display_rows();
            for r in &rows {
                println!("{:6}  {:>10}  {}", r.label, r.value_str, r.unit);
            }
            println!("---");
        }
        std::thread::sleep(Duration::from_millis(100));
    }
}
